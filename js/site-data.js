// Load content from localStorage or use default
function loadContent() {
  try {
    const storedVersion = localStorage.getItem('sportsDrillsContentVersion');
    if (storedVersion !== CONTENT_VERSION) {
      localStorage.removeItem('sportsDrillsContent');
      localStorage.setItem('sportsDrillsContentVersion', CONTENT_VERSION);
      return DEFAULT_CONTENT;
    }
    const stored = localStorage.getItem('sportsDrillsContent');
    if (stored) {
      return JSON.parse(stored);
    }
  } catch (e) {
    console.warn('Failed to load content from localStorage:', e);
  }
  return DEFAULT_CONTENT;
}

// Save content to localStorage
function saveContent(content) {
  try {
    localStorage.setItem('sportsDrillsContent', JSON.stringify(content));
    return true;
  } catch (e) {
    console.warn('Failed to save content to localStorage:', e);
    return false;
  }
}

// Get featured drills (static, no daily rotation)
function getDrillsToday() {
  const content = loadContent();
  // Return first 5 drills consistently, no date-based rotation
  return content.drills.slice(0, 5).map(drill => ({ ...drill, isToday: true }));
}

// Get latest news
function getLatestNews(count = 6) {
  const content = loadContent();
  return content.news
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, count);
}

// Get news by category
function getNewsByCategory(category) {
  const content = loadContent();
  return content.news.filter(n => n.category === category);
}

// Get drills by sport
function getDrillsBySport(sport) {
  const content = loadContent();
  return content.drills.filter(d => d.sport === sport);
}

// Get coach by ID
function getCoach(id) {
  const content = loadContent();
  return content.coaches.find(c => c.id === id);
}

// Get all coaches
function getAllCoaches() {
  return loadContent().coaches;
}

// Format date
function formatDate(dateStr) {
  const options = { year: 'numeric', month: 'long', day: 'numeric' };
  return new Date(dateStr).toLocaleDateString('id-ID', options);
}

// Export for use in other scripts
if (typeof window !== 'undefined') {
  window.SportsDrillsData = {
    DEFAULT_CONTENT,
    loadContent,
    saveContent,
    getDrillsToday,
    getLatestNews,
    getNewsByCategory,
    getDrillsBySport,
    getCoach,
    getAllCoaches,
    formatDate
  };
}
